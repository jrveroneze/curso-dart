void main() {
	// Variavel de caracter (String)
	String nome = 'Junior';
	// nome = '${nome} Veroneze'; // Exemplo de reescrita de variaveis
	print(nome);

	// Variavel de inteiros (int)
	int idade = 24;
	print('Sua idade é: ${idade}');

	// Variavel de valores fracionados (Double)
	double altura = 1.94;
	print('Sua altura é: ${altura}');

	// Variavel booleana (Bool)
	bool juniorDev = true;
	print(juniorDev);
}